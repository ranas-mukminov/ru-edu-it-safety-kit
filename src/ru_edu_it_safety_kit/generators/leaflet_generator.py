from ru_edu_it_safety_kit.models.school_profile import SchoolProfile


class LeafletGenerator:
    @staticmethod
    def generate_for_parents(profile: SchoolProfile) -> str:
        return f"""# Памятка для родителей
Уважаемые родители!

В учебном заведении "{profile.name}" уделяется большое внимание безопасности детей в цифровой среде.

Мы обеспечиваем:
1. Контентную фильтрацию доступа к нежелательным ресурсам.
2. Защищенный доступ к образовательным порталам.

Рекомендации для дома:
- Установите "Родительский контроль" на личных устройствах ребенка.
- Обсуждайте с детьми правила общения в социальных сетях.
- При возникновении инцидентов кибербуллинга сообщайте классному руководителю.

С уважением,
Администрация {profile.name}
"""

    @staticmethod
    def generate_for_students(profile: SchoolProfile) -> str:
        return f"""# Правила безопасного интернета для учащихся

Ты учишься в "{profile.name}". Здесь работает быстрый и безопасный интернет.

ЗАПОМНИ:
1. Школьный Wi-Fi предназначен для учебы.
2. Не заходи на подозрительные сайты и не скачивай неизвестные файлы.
3. Не сообщай никому свои пароли (даже друзьям).
4. Если увидел в сети что-то пугающее или странное — скажи учителю.

Твоя безопасность — в твоих руках!
"""

    @staticmethod
    def generate_for_teachers(profile: SchoolProfile) -> str:
        text = f"""# Памятка по информационной безопасности для учителей

Организация: {profile.name}

1. **Пароли**: Используйте сложные пароли (минимум 8 символов), не записывайте их на стикерах.
2. **Персональные данные**: Обработка оценок и данных учеников — только в защищенных системах.
3. **Флешки**: Проверяйте все внешние носители антивирусом перед открытием файлов.
4. **Инциденты**: При обнаружении попыток обхода фильтрации или вирусной активности — немедленно сообщите
   системному администратору.
"""
        if profile.is_multi_building:
            text += (
                "\n5. **Удаленный доступ**: При работе из другого корпуса используйте только штатные "
                "средства доступа.\n"
            )

        return text
